    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            int id = Integer.parseInt(jTxtPedido.getText());
            Pedido pedido = new PedidoDAO().listarPedidoId(id);
            
            if (pedido == null) {
                JOptionPane.showMessageDialog(null, "Pedido não encontrado!");
                return;
        }

            jTxtNumeroPedido.setText(String.valueOf(pedido.getId()));
            jTxtData.setText(String.valueOf(pedido.getData()));
            jCbxStatus.setSelectedItem(pedido.getStatus());
            jTxtValorTotal.setText(String.valueOf(pedido.getTotal()));
            jTxtAreaObs.setText(pedido.getObservacoes());
            jBtnAdicionar.setEnabled(false);
            jBtnEditar.setEnabled(true);
            jBtnExcluir.setEnabled(true);
            jBtnImprimir.setEnabled(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

