    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
        // TODO add your handling code here:
        if ((jTxtNumeroPedido.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Busque um pedido!!!");
            
            } else {
                int ex = JOptionPane.showConfirmDialog(null, "Realmente deseja excluir esse pedido?");
            
                if (ex == JOptionPane.YES_OPTION){
                Pedido pedido = new Pedido();
                int numPed = Integer.parseInt(jTxtNumeroPedido.getText());
                pedido.setId(numPed);
                PedidoDAO dao = new PedidoDAO();
                dao.excluirPedido(pedido);
                listarCliente();
                jTxtNumeroPedido.setText("");
                jTxtData.setText("");
                jCbxStatus.setSelectedIndex(0);
                jTxtClienteNome.setText("");
                jTxtID.setText("");
                jTxtValorTotal.setText("");
                jTxtAreaObs.setText("");
                jBtnAdicionar.setEnabled(true);
                jBtnEditar.setEnabled(false);
                jBtnExcluir.setEnabled(false);
                jBtnImprimir.setEnabled(false);
            }
        }
    }//GEN-LAST:event_jBtnExcluirActionPerformed

    private void jBtnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnPesquisarActionPerformed
        // TODO add your handling code here:        
        String nome = "%" + jTxtClienteNome.getText() + "%";

        ClienteDAO dao = new ClienteDAO();
        List<Cliente> lista = dao.listarClienteNome(nome);
        DefaultTableModel dados = (DefaultTableModel) jTblClientesPedido.getModel();
        dados.setNumRows(0);

        for (Cliente c : lista) {
            dados.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getTelefone()});
        }
    }//GEN-LAST:event_jBtnPesquisarActionPerformed

