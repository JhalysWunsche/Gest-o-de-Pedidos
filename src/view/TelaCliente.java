    public void listar(){
        ClienteDAO dao = new ClienteDAO();
        
        List<Cliente>  lista = dao.listarClientes();
        DefaultTableModel dados = (DefaultTableModel) jTblCliente.getModel();
        dados.setNumRows(0);

        for (Cliente c:lista) {
            dados.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getDocumento(),
                c.getTipoCliente(),
                c.getTelefone(),
                c.getEmail(),
                c.getLogradouro(),
                c.getNumero(),
                c.getComplemento(),
                c.getBairro(),
                c.getCidade(),
                c.getEstado(),
                c.getCep(),
                c.getObservacoes(),
            });

        }
    }

