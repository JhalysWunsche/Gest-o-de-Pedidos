    private void jTblClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTblClienteMouseClicked

        
        int selectedRow = jTblCliente.getSelectedRow();
        if (selectedRow != -1) {  // só acessa se houver linha selecionada
            jTxtNome.setText(String.valueOf(jTblCliente.getValueAt(selectedRow, 1)));
            jTxtDocumento.setText(String.valueOf(jTblCliente.getValueAt(selectedRow, 2)));
            jTxtTelefone.setText(String.valueOf(jTblCliente.getValueAt(selectedRow, 4)));
            jTxtEmail.setText(String.valueOf(jTblCliente.getValueAt(selectedRow, 5)));
            jTxtEndereco.setText(String.valueOf(jTblCliente.getValueAt(selectedRow, 6)));
            jTxtNumero.setText(String.valueOf(jTblCliente.getValueAt(selectedRow, 7)));
            jTxtComplemento.setText(String.valueOf(jTblCliente.getValueAt(selectedRow, 8)));
            jTxtBairro.setText(String.valueOf(jTblCliente.getValueAt(selectedRow, 9)));
            jTxtCidade.setText(String.valueOf(jTblCliente.getValueAt(selectedRow, 10)));
            jCbxEstado.setSelectedItem(String.valueOf(jTblCliente.getValueAt(selectedRow, 11)));
            jTxtCep.setText(String.valueOf(jTblCliente.getValueAt(selectedRow, 12)));
            jTxtObs.setText(String.valueOf(jTblCliente.getValueAt(selectedRow, 13)));

            String tipo = jTblCliente.getValueAt(selectedRow, 3).toString();
            if (tipo.equals("Pessoa Física")) {
                jBtnFisico.setSelected(true);
            } else {
                jBtnJuridico.setSelected(true);
            }

            jBtnAdicionar.setEnabled(false);
            jBtnEditar.setEnabled(true);
            jBtnExcluir.setEnabled(true);
        }
    }//GEN-LAST:event_jTblClienteMouseClicked

