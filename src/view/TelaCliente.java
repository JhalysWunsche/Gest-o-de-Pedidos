    private void jBtnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEditarActionPerformed
        // TODO add your handling code here:
        if ((jTxtNome.getText().isEmpty() || jTxtDocumento.getText().isEmpty())){
                JOptionPane.showMessageDialog(null, "Preencha os campos de Nome e Documento!!!");
        } else {
            String nome = jTxtNome.getText();
            String documento = jTxtDocumento.getText();
            String tipoCliente = "";
            if (jBtnFisico.isSelected()) {
                tipoCliente = "Pessoa Física";
                } else if (jBtnJuridico.isSelected()) {
                    tipoCliente = "Pessoa Jurídica";
                }
            String email = jTxtEmail.getText();
            String telefone = jTxtTelefone.getText();
            String cep = jTxtCep.getText();
            String logradouro = jTxtEndereco.getText();
            String estado = jCbxEstado.getSelectedItem().toString();
            String numero = jTxtNumero.getText();
            String cidade = jTxtCidade.getText();
            String bairro = jTxtBairro.getText();
            String complemento = jTxtComplemento.getText();
            String observacoes = jTxtObs.getText();
            
            int id = (int) jTblCliente.getValueAt(jTblCliente.getSelectedRow(), 0);

            Cliente cliente = new Cliente(nome,  documento,  tipoCliente,  telefone,  email,  numero,  complemento,  cidade,  estado,  cep,  observacoes,  logradouro,  bairro);
            cliente.setId(id);
            ClienteDAO dao = new ClienteDAO();
            dao.alterarCliente(cliente);
            jTxtNome.setText("");
            jTxtDocumento.setText("");
            groupBtnTipoCliente.clearSelection();
            jTxtEmail.setText("");
            jTxtTelefone.setText("");
            jTxtCep.setText("");
            jTxtEndereco.setText("");
            jTxtNumero.setText("");
            jCbxEstado.setSelectedItem("AC");
            jTxtCidade.setText("");
            jTxtBairro.setText("");
            jTxtComplemento.setText("");
            jTxtObs.setText("");
            jBtnAdicionar.setEnabled(true);
            jBtnEditar.setEnabled(false);
            jBtnExcluir.setEnabled(false);
        }
    }//GEN-LAST:event_jBtnEditarActionPerformed

    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
           // TODO add your handling code here:
           if ((jTxtNome.getText().isEmpty()) || (jTxtDocumento.getText().isEmpty())
           || (jTxtEmail.getText().isBlank()) || (jTxtTelefone.getText().isEmpty())) {
           JOptionPane.showMessageDialog(null, "Preencha todos os campos!!!");
           
           } else {
               int ex = JOptionPane.showConfirmDialog(null, "Realmente deseja excluir esse usuário?");
               if (ex == JOptionPane.YES_OPTION){
               Cliente clientes = new Cliente();
               clientes.setId((int) jTblCliente.getValueAt(jTblCliente.getSelectedRow(), 0));
               ClienteDAO dao = new ClienteDAO();
               dao.excluirCliente(clientes);
               jTxtNome.setText("");
               jTxtDocumento.setText("");
               groupBtnTipoCliente.clearSelection();
               jTxtEmail.setText("");
               jTxtTelefone.setText("");
               jTxtCep.setText("");
               jTxtEndereco.setText("");
               jTxtNumero.setText("");
               jCbxEstado.setSelectedItem("AC");
               jTxtCidade.setText("");
               jTxtBairro.setText("");
               jTxtComplemento.setText("");
               jTxtObs.setText("");
               jBtnAdicionar.setEnabled(true);
               jBtnEditar.setEnabled(false);
               jBtnExcluir.setEnabled(false);
            }
        }
    }//GEN-LAST:event_jBtnExcluirActionPerformed

